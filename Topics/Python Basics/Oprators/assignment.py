
x = 5
y = 10  
x += y  # x = x + y
print(x)
x -= y  # x = x - y
print(x)

x *= y  # x = x * y
print(x)

x /= y  # x = x / y
print(x)

x %= y  # x = x % y
print(x)

x //= y  # x = x // y
print(x)

x **= y  # x = x ** y
print(x)

